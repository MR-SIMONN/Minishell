#include "../../Minishell.h"

void	skip_it(char *s, int *i, char c)
{
	while (s[*i] && s[*i] == c)
		(*i)++;
}

void	skip_spaces(char *s, int *i)
{
	while (s[*i] && is_space(s[*i]))
		(*i)++;
}
