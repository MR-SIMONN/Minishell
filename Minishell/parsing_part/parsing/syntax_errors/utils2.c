#include "../../../Minishell.h"

int	parentheses(char *s)
{
	int		i;
	int		f;
	char	c;

	i = 0;
	f = 0;
	while (s[i])
	{
		if ((s[i] == '\'' || s[i] == '\"') && !f)
		{
			c = s[i];
			f = 1;
		}
		else if (s[i] == c && f)
			f = 0;
		if ((s[i] == '(' || s[i] == ')') && !f)
			return (1);
		i++;
	}
	return (0);
}
