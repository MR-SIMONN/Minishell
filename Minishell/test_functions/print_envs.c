#include "../Minishell.h"

void	print_envs(t_env *env)
{
    printf("\n---  env list  -------------------------------------------------------\n");
	while (env)
	{
		if (env->key && env->value)
			printf("%s=%s\n", env->key, env->value);
		else if (env->key)
			printf("%s=\n", env->key);
		env = env->next;
	}
    printf("-----------------------------------------------------------------------\n");
}
